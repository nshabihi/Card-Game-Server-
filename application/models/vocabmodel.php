class Vocabmodel extends CI_Model {

    public function __construct()
    {

        $this->load->database();

    }
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
    public function checkVocabValidity($vocab)
    {
        $this -> db -> select("id");
        $this -> db -> from("vocabularies");
        $this -> db -> where("vocab", $vocab);
        $query = $this -> db -> get();
        $result = $query -> result_array();

        if($query -> num_rows() > 0)
        {
            return $result[0]['id'];
        }else
        {
            return -2;
        }
    }
    //
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //
};
